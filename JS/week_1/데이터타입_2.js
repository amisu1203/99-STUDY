// 3. Boolean : true와 false 값
let bool1 = true;
let bool2 = false;

console.log(typeof bool1);
console.log(typeof bool2);

// 4. undefined
let nothing;
console.log(nothing, typeof nothing); //undefined, undefined

// 5. Null : 값이 존재하지 않음을 명시적으로 나타내는 데이터 타입
let y = null;
console.log(y, typeof y);

// 6. Object
let person = {
  name: "jisu",
  age: 12,
};
console.log(person, typeof person);

// 7. array
let fruits = ["apple", "banana", "watermelon"];
console.log(fruits, typeof fruits);
